package com.thetechbull.controlStraments;

public class Example2Loops {
    public static void main(String[] args) {
        //when we have to use while loop

        //print 1 to 10 numbers
        int i = 1;
        while(i<=10){
            System.out.println(i);
            i++;
        }

        System.out.println("*******************************************");
        //print 10 to 1 numbers
        int j = 10;
        while(j>=1){
            System.out.println(j);
            j--;
        }
        //when we have to use do while loop
        System.out.println("*******************************************");
        //print 1 to 10 numbers
        int k = 1;

        do {
            System.out.println(k);
            k++;
        }while(k<=10);
        //when we have to use for loop
      }
}
